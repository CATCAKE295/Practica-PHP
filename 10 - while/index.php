
<h1>While ascendente</h1>
<?php 

$i = 0;

while ($i <= 10) {
    echo 'numero: ' . $i . '<br />';
    $i++; 
}

?>


<br>


<h1>While descendente</h1>
<?php 

$j = 10;

while ($j >= 0) {
    echo 'Numero: ' .$j. ' <br />';
    $j--;
}


?>


<br>

<h1>Ciclo Do While ascendente</h1>

<?php 

$k = 0;

do {
    echo 'numero: ' . $k . '<br />';
    $k++;
} while ($k <= 10);

?>


<br>

<h1>Ciclo Do While descendente</h1>

<?php 

$l = 10;

do {

    echo 'Numero: ' .$l. ' <br />';
    $l--;
} while ($l >= 0);


?>